namespace Bhp\Util\Qrcode\Lib;


class QrRsBlock
{
    public $dataLength;
    public $data = [];
    public $eccLength;
    public $ecc = [];

    public function __construct($dl, $data, $el, &$ecc, QrRsItem $rs)
    {
        $rs->encode_rs_char($data, $ecc);

        $this->dataLength = $dl;
        $this->data = $data;
        $this->eccLength = $el;
        $this->ecc = $ecc;
    }
}
