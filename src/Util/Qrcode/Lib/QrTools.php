namespace Bhp\Util\Qrcode\Lib;

class QrTools
{
    /**
     * 二值化
     * @param array $frame
     * @return array
     */
    public static function binarize(array $frame): array
    {
        $len = count($frame);
        foreach ($frame as &$frameLine) {

            for ($i = 0; $i < $len; $i++) {
                $frameLine[$i] = (ord($frameLine[$i]) & 1) ? '1' : '0';
            }
        }
        return $frame;
    }
}

